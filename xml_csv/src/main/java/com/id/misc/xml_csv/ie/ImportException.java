package com.id.misc.xml_csv.ie;

/**
 * @author idanilov
 * 
 */
public class ImportException extends Exception {

	public ImportException() {
	}

	public ImportException(final String m) {
		super(m);
	}

	public ImportException(final Throwable cause) {
		super(cause);
	}

	public ImportException(final String m, final Throwable cause) {
		super(m, cause);
	}

}
