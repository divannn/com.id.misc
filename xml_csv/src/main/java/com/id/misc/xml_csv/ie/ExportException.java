package com.id.misc.xml_csv.ie;

/**
 * @author idanilov
 * 
 */
public class ExportException extends Exception {

	public ExportException() {
	}

	public ExportException(final String m) {
		super(m);
	}

	public ExportException(final Throwable cause) {
		super(cause);
	}

	public ExportException(final String m, final Throwable cause) {
		super(m, cause);
	}

}
